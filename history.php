  		<p>In 2001, some 40 years later, I was turning 13, and with the benefit of my father’s basement workshop, I too had developed a love for building and design.  My Dad told me that when he was my age he had started to build a Paper Tiger, an iconic and ultralight wooden sailboat of New Zealand design.</p>
  		<p class="collapse" id="block-id1">This explained the two large and unfinished boat hulls in the basement of my family’s home in Rhode Island; they had been there for as long as I could remember.<br><br>
  		In 2001, my father had an idea rooted in nostalgia.  I, his oldest son, was just about the age he was when he first began building the Paper Tiger, and I think I had finally started to impress him with my woodworking capabilities.
  		<br><br>
  		He suggested I pick up where he left off.
