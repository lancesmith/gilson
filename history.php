  		<p>My name is Nicholas Gilson, and I believe John Lennon said it best when he wrote, “Life is what happens to you while you’re busy making other plans.</p>
  		
  		<p>At the age of 14, my father was moved against his will from New Zealand to America in the hull of a steamship.  His father had died years earlier, and his mother (my grandmother) had remarried an American Navy man.</p>
  		
  		<p>Having been relocated to the landlocked state of Pennsylvania, my father longed to stay connected to his Kiwi roots, and in large part, that meant staying connected to sailing.  He had built his first sailboat from a broom, sheet, and tire when he was five years old.  My grandmother can be seen in an old photograph calmly watching her child venture out into the deep waters of Evans Bay in a small boat he had created.  New Zealanders are the antithesis of the helicopter parent.</p>
  		
  		<p>Now in America, my 14-year-old father started crafting his next boat, a project that would span generations.</p>
